package src.java;

public class Loop {
    public static void main(String[] arg) {
        int  range = 10 ;
        for(int i =1 ; i<=range ; i++){
            System.out.print("   "+i);
        }
        int i=1;
        while(i<=10){
            System.out.println(i);
            i++;
        }
    }
}
